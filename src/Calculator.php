<?php

/**
 * Calculator class
 */
class Calculator {
    
    public function addition($x, $y){
        return $x + $y;
    }
    
    public function substraction($x, $y){
        return $x - $y;
    }
    
    public function multiplication($x, $y){
        return $x * $y;
    }
    
    public function division($x, $y){
        return $x / $y;
    }
}